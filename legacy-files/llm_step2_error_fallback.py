# @title LLM response로부터 JSON 뽑는 함수
def extract_json_from_response(response):
    '''
    LLM의 응답을 JSON 형식으로 파싱하여 dict를 반환하는 함수
    '''
    # regex를 사용하여 dictionary 부분만 추출
    pattern = re.compile(r'\{.*\}', re.DOTALL)
    match = pattern.search(response)

    if match:
        json_string = match.group()

        try:
            # 정규 표현식을 사용하여 모든 주석 제거 (// 스타일 주석과 /* */ 스타일 주석)
            json_string = re.sub(r'//.*?(?=\n)|/\*.*?\*/', '', json_string, flags=re.S)

            # 문자열을 Python dictionary로 변환
            result_dict = json.loads(json_string)
            return result_dict, json_string

        except json.JSONDecodeError as e:
            print("JSON is invalid")
            print('JSON string :\n', json_string)
            print(f"Error: {e}")
            return None, json_string
    else:
        print("LLM output에서 dictionary pattern을 추출할 수 없습니다.")
        return None, None

