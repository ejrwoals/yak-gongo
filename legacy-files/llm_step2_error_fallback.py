    transformed_data = {}
    for key, value in data_dict.items():
        if value is not None:
            if key in ['링크']:
                transformed_data[key] = {'url': value}
            elif key in ['등록일']:
                transformed_data[key] = {'date': {'start': value, 'end': None}}
