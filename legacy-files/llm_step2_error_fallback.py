                gpt_error = '''
                [ ERROR 발생 ] "급여", "시급", "일회성 근무 시급" 값이 모두 없습니다.
                공고에 급여 명시 여부 = True 가 맞는지 재확인이 필요합니다. [ 확인 요망 ]
                '''
                error_history = error_message(gpt_error, error_history)
