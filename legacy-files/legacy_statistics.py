# @title 함수) 노션 "Colab 통계 output 연동 페이지"에 통계 결과 값들 업데이트 하는 함수
def round_values(item, n):
    # 숫자일 경우 소수점 n번째 자리까지 반올림
    if isinstance(item, float):
        return round(item, n)
    # 딕셔너리일 경우 각 값에 대해 소수점 n번째 자리까지 반올림
    elif isinstance(item, dict):
        return {k: round(v, n) for k, v in item.items()}
    # pandas.Series일 경우 각 값에 대해 소수점 n번째 자리까지 반올림
    elif isinstance(item, pd.Series):
        return item.round(n)
    # 그 외의 경우 원래 값 반환
    else:
        return item

def update_synced_blocks(page_id, output_values, imgur_client_id):
    # Notion 페이지 가져오기
    page = notion.pages.retrieve(page_id=page_id)

    # 블록 가져오기
    blocks = notion.blocks.children.list(block_id=page_id)

    # Synced 블록 개수 초기화
    synced_block_count = 0

    # Synced 블록 ID 리스트
    synced_block_ids = []

    # 블록 내용 확인 및 Synced 블록 ID 저장
    for block in blocks['results']:
        # 블록 내용 출력하기
        # print('[', block['type'], '블록 ]')
        # print(block)
        # print('id :', block['id'])
        # print('세부사항 :', block[block['type']], '\n')

        if block['type'] == 'synced_block':
            synced_block_count += 1
            synced_block_ids.append(('synced', block['id']))

    print('Synced Block 개수 : ', synced_block_count)
    print('업데이트 할 output values의 개수 : ', len(output_values), '\n\n')

    # 모든 output values에 대해 round 처리
    rounded_output_values = [round_values(item, 2) for item in output_values]

    # Synced 블록과 output values 매핑
    update_dict = dict(zip(synced_block_ids, rounded_output_values))

    # Synced 블록 업데이트
    for id, value in update_dict.items():
        if id[0] == 'synced':  # synced_block_id가 있는 경우
            synced_block_id = id[1]

            print(type(value))
            if isinstance(value, datetime.date):
                clear_block_children(notion, synced_block_id)
                update_synced_block_paragraph(notion, value.strftime("%Y-%m-%d"), synced_block_id=synced_block_id)

            elif isinstance(value, int):
                clear_block_children(notion, synced_block_id)
                update_synced_block_paragraph(notion, str(value) + ' 개', synced_block_id=synced_block_id)

            elif isinstance(value, np.float64):
                clear_block_children(notion, synced_block_id)
                update_synced_block_paragraph(notion, str(value) + ' 만원', synced_block_id=synced_block_id)

            elif isinstance(value, dict):
                clear_block_children(notion, synced_block_id)
                add_vertical_table_to_block(notion, synced_block_id, value)

            elif isinstance(value, str):
                clear_block_children(notion, synced_block_id)
                add_image_to_block(value, imgur_client_id, notion, synced_block_id)
            else:
                print('[오류] Synced 블록에 값을 업데이트하려면 value가 float, dictionary 또는 string 형태여야 합니다.')

