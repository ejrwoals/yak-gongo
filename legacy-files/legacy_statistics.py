# @title Synced Block을 새로 추가하는 코드 : add_synced_block
def add_synced_block(notion, page_id, my_text):
    """
    Notion 페이지에 Synced Block을 추가하고, 해당 블록에 텍스트를 가진 단락을 삽입합니다.

    Args:
        notion: Notion 클라이언트 인스턴스입니다.
        page_id (str): Synced Block을 추가할 Notion 페이지의 ID입니다.
        my_text (str): Synced Block 안에 추가할 텍스트 내용입니다.

    Returns:
        str: 생성된 Synced Block의 ID를 반환합니다.
    """
    # Synced Block을 생성합니다.
    response = notion.blocks.children.append(
        block_id=page_id,
        children=[
            {
                "object": "block",
                "type": "synced_block",
                "synced_block": {
                    "synced_from": None  # 새로운 Synced Block 생성
                }
            }
        ]
    )

    # 생성된 Synced Block의 ID를 가져옵니다.
    synced_block_id = response["results"][0]["id"]

    # Synced Block에 자식 블록(단락)을 추가합니다.
    response_2 = notion.blocks.children.append(
        block_id=synced_block_id,
        children=[
            {
                "object": "block",
                "type": "paragraph",
                "paragraph": {
                    "rich_text": [
                        {
                            "type": "text",
                            "text": {
                                "content": my_text
                            }
                        }
                    ]
                }
            }
        ]
    )

    # 생성된 Paragraph Block의 ID를 가져옵니다.
    paragraph_block_id = response_2["results"][0]["id"]

    print("Synced block added to Notion page.")
    return synced_block_id, paragraph_block_id

