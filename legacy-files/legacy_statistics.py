# @title linear regression으로 구한 회귀선 방정식을 input으로 넣으면 근무시간에 따른 월급여 그래프를 그려주는 함수

def plot_monthly_wage(data, fig_title='주당 근무 시간에 따른 월급',
                      slope=None, intercept=None,
                      custom_figsize=(10,10), xticks_interval=1):

    if slope is None or intercept is None:
        # linear regression 으로 회귀선 구하기
        slope, intercept = np.polyfit(data['시간/week'], data['시급(엄밀히)'], 1)
    else:
        pass

    # 1차 회귀선 방정식
    equation = f"y = {slope:.4f}x + {intercept:.3f}"

    # 그래프 그리기
    x_values = np.linspace(0, data['시간/week'].max(), 100)  # x축: 주당 근무 시간, 0에서 60시간 범위로 가정
    y_values = slope * x_values + intercept  # 회귀선 방정식
    integral_y_values = [x_val * y_val * 4.34 for x_val, y_val in zip(x_values, y_values)]  # 주급에 4.34주를 곱해서 월급으로 환산하여 적분된 함수 값 계산

    # data의 시급을 월급으로 환산
    data_monthly = data.copy()
    data_monthly['월급'] = data_monthly['시급(엄밀히)'] * data_monthly['시간/week'] * 4.34

    plt.figure(figsize=custom_figsize)

    # scatter plot
    sns.scatterplot(data=data_monthly, x='시간/week', y='월급', s=50, color='black', alpha=0.2)

    # regression line
    # plt.plot(x_values, y_values, label=f"회귀선 : {equation}", linestyle='--')
    plt.plot(x_values, integral_y_values, label=f"regression line", color='blue')

    # 그래프 제목 설정
    plt.title(fig_title + '\n', fontsize=18, fontweight='bold')

    # y축
    y_start = 0
    y_end = data_monthly['월급'].max() + 30
    plt.ylim(y_start, y_end)
    plt.yticks(np.arange(y_start, y_end + 50, 50))

    # x축
    x_start = max(0, data_monthly['시간/week'].min() - 2)
    x_end = data_monthly['시간/week'].max() + 3
    plt.xlim(x_start, x_end)
    plt.xticks(np.arange(x_start, x_end, xticks_interval))

    plt.xlabel("주당 근무 시간 (단위 : 시간)")
    plt.ylabel("월급 (단위 : 만원)")
    plt.legend()
    plt.grid(True)

    # 레이아웃 조정
    plt.tight_layout()

    # 그래프 저장
    plt.savefig('{}.png'.format(fig_title), format='png', dpi=300)

    # 이미지에 워터마크 추가 (예시 함수, 사용자 정의 함수로 수정 필요)
    add_watermark(image_file_name=fig_title, rotation_angle=15, font_size_parameter=10)

    plt.show()


