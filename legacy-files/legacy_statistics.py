# @title 기존 Synced Block의 내용을 편집하는 함수 : update_synced_block_paragraph
def update_synced_block_paragraph(notion, new_content, paragraph_block_id=None, synced_block_id=None):
    """
    기존 Synced Block의 단락 블록 내용을 업데이트하는 함수입니다.

    Parameters:
    - notion: Notion 클라이언트 인스턴스
    - new_content: 업데이트할 새로운 텍스트 내용 (문자열)
    - paragraph_block_id: 단락 블록의 ID (선택 사항)
    - synced_block_id: Synced Block의 ID (선택 사항)

    주의:
    - paragraph_block_id 또는 synced_block_id 중 하나는 반드시 제공되어야 합니다.
    """

    if paragraph_block_id:
        # 단락 블록의 텍스트를 업데이트합니다.
        try:
            response = notion.blocks.update(
                block_id=paragraph_block_id,
                paragraph={
                    "rich_text": [
                        {
                            "type": "text",
                            "text": {
                                "content": new_content
                            }
                        }
                    ]
                }
            )
            print(f"Synced 블록의 단락 텍스트가 '{new_content}'로 업데이트되었습니다.")
        except Exception as e:
            print(f"❌ Synced 블록의 단락 텍스트 업데이트에 실패했습니다: {e}")
    elif synced_block_id:
        # Synced Block의 자식 블록들을 가져옵니다.
        try:
            response = notion.blocks.children.list(block_id=synced_block_id)
            # 자식 블록 중에서 단락 블록을 찾습니다.
            paragraph_block_id = None
            for child in response['results']:
                if child['type'] == 'paragraph':
                    paragraph_block_id = child['id']
                    break  # 첫 번째 단락 블록을 찾으면 종료합니다.

            if paragraph_block_id is None:
                # Synced Block에 자식 블록(단락)을 추가합니다.
                response_2 = notion.blocks.children.append(
                    block_id=synced_block_id,
                    children=[
                        {
                            "object": "block",
                            "type": "paragraph",
                            "paragraph": {
                                "rich_text": [
                                    {
                                        "type": "text",
                                        "text": {
                                            "content": new_content
                                        }
                                    }
                                ]
                            }
                        }
                    ]
                )

                print(f"Synced 블록의 단락 텍스트가 '{new_content}'로 업데이트되었습니다.")

            else:
                # 단락 블록의 텍스트를 업데이트합니다.
                response = notion.blocks.update(
                    block_id=paragraph_block_id,
                    paragraph={
                        "rich_text": [
                            {
                                "type": "text",
                                "text": {
                                    "content": new_content
                                }
                            }
                        ]
                    }
                )
                print(f"Synced 블록의 단락 텍스트가 '{new_content}'로 업데이트되었습니다.")
        except Exception as e:
            print(f"❌ Synced 블록의 단락 텍스트 업데이트에 실패했습니다: {e}")
    else:
        print("❌ Synced 블록의 paragraph_block_id 또는 synced_block_id 중 하나를 제공해야 합니다.")

