# @title Bold체 font 경로
watermark_font_path = '/usr/share/fonts/opentype/noto/NotoSerifCJK-Bold.ttc'

def add_watermark(image_file_name, watermark_text='CJM',
                  rotation_angle=20, font_size_parameter=7):
    image_path = '/content/{}.png'.format(image_file_name)
    output_path = '/content/{}_워터마크.png'.format(image_file_name)

    image = Image.open(image_path).convert("RGBA")

    # 워터마크 텍스트의 폰트 및 크기 설정
    font_size = int(image.height * font_size_parameter/100) # 이미지 세로 길이의 15% 수준으로 폰트 크기를 설정.
    font = ImageFont.truetype(watermark_font_path, font_size)  # 굵은 폰트 사용

    draw = ImageDraw.Draw(image)
    # textbbox를 사용하여 텍스트의 경계 박스 얻기
    left, top, right, bottom = draw.textbbox((0, 0), text=watermark_text, font=font)

    # 텍스트의 너비와 높이 계산
    text_width = right - left
    text_height = bottom - top

    # 워터마크를 그릴 투명한 이미지 생성
    padding = 200 # 워터마크의 일부분이 잘리는 문제를 해결하기 위한 여백 추가
    text_image = Image.new('RGBA', (text_width, text_height + padding), (0, 0, 0, 0))
    draw = ImageDraw.Draw(text_image)

    # 그림자 그리기
    shadow_color = (0, 0, 0, 128)  # 반투명 검정색 : 제일 마지막에 128이 불투명도를 설정하는 숫자임.
    draw.text((2, 2), watermark_text, font=font, fill=shadow_color)

    # 텍스트 그리기
    text_color = (255, 255, 255, 204)  # 반투명 흰색 : 204면 불투명도 80% 수준임. (d.t. 204/255 = 0.8)
    draw.text((0, 0), watermark_text, font=font, fill=text_color)

    # 텍스트 이미지 회전
    rotated_text_image = text_image.rotate(rotation_angle, expand=True)

    # 회전된 텍스트 이미지를 원본 이미지의 중앙에 배치
    x = (image.width - rotated_text_image.width) // 2
    y = (image.height - rotated_text_image.height) // 2

    # 투명한 이미지에 회전된 텍스트 이미지를 붙이기
    tmp_img = Image.new('RGBA', image.size, (0,0,0,0))
    tmp_img.paste(rotated_text_image, (int(x), int(y)), rotated_text_image)

    # 이미지 합치기
    watermarked = Image.alpha_composite(image, tmp_img)

    # 이미지 저장
    watermarked.save(output_path, "PNG")

